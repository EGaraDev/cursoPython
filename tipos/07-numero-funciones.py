import math

print(round(1.3))  # redonde el numero al entero mas cercano
print(round(1.7))
print(round(1.5))  # a la mitad siempre redondea para arriba

print(abs(-77))  # retorna el valor absoluto


print(math.ceil(1.1))  # redondea siempre para arriba
print(math.floor(1.999999))  # redondea siempre para abajo
print(math.isnan(23))  # retorna booleano True si no es un numero False si es un numero
#print(math.isnan("23")) genera error ya que unicamente se puede ingresar numeros como parametro
print(math.pow(10,3))  # eleva el primer numero al segundo numero
print(math.sqrt(9))  # realiza la raiz cuadrada del numero ingresado
