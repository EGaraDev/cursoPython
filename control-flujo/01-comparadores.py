print(1 > 2)  # evalua si el primero es mayor que el segundo
print(1 < 2)  # evalua si el primero es menor que el segundo
print(1 >= 2)  # evalua si el primero es mayor o igual que el segundo
print(1 <= 2)  # evalua si el primero es menor o igual que el segundo
print(1 == 2)  # evalua si son iguales
print(1 != 2)  # evalua si son distintos